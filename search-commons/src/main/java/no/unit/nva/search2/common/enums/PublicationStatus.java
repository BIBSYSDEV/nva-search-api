/**
 * PublicationStatus is an enum that represents the different states a publication can be in.
 * <p>
 * Values -> DELETED, DRAFT, DRAFT_FOR_DELETION, NEW, PUBLISHED, PUBLISHED_METADATA, UNPUBLISHED
 * </p>
 */
public enum PublicationStatus {
   DELETED,
   DRAFT,
   DRAFT_FOR_DELETION,
   NEW,
   PUBLISHED,
   PUBLISHED_METADATA,
   UNPUBLISHED
}
